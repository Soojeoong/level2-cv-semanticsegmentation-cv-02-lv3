class XRayInferenceDataset(Dataset):
    def __init__(self,image_root="train/DCM", transforms=None):
        self.image_root=image_root

        pngs = {
            os.path.relpath(os.path.join(root, fname), start=image_root)
            for root, _dirs, files in os.walk(image_root)
            for fname in files
            if os.path.splitext(fname)[1].lower() == ".png"
        }

        _filenames = pngs
        _filenames = np.array(sorted(_filenames))
        
        self.filenames = _filenames
        self.transforms = transforms
    
    def __len__(self):
        return len(self.filenames)
    
    def __getitem__(self, item):
        image_name = self.filenames[item]
        image_path = os.path.join(self.image_root, image_name)
        
        # Read and normalize image
        image = cv2.imread(image_path)
        image = image / 255.0  # Normalize image to [0, 1]

        if self.transforms is not None:
            inputs = {"image": image}
            result = self.transforms(**inputs)
            image = result["image"]

        # Apply transpose and convert to Tensor if needed
        if isinstance(image, np.ndarray):  # Ensure it's a numpy array
            image = image.transpose(2, 0, 1)  # Convert HWC to CHW
            image = torch.from_numpy(image).float()  # Convert to Tensor

        return image, image_name


